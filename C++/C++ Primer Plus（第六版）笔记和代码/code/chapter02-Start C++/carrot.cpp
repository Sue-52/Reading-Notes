#include <iostream>

int main()
{
	using namespace std;

	// 声明变量
	// int: 存储数据的类型
	// carrots： 变量名
	int carrots;
	// 赋值变量
	carrots = 25;

	// 输出现在的结果
	cout << "I have " << carrots << " carrots" << endl;

	// 运算符
	carrots = carrots - 1;

	// 输出最终运算后的结果
	cout << "Crunch, crunch. Now I have " << carrots << " carrots" << endl;

	// 结束语句
	return 0;
}